window.getLangStr = function(key) {
    const lang = localStorage.getItem('eureka_lang') || 'en';
    const dict = translations[lang] || translations['en'];
    return dict[key] || translations['en'][key] || key;
};

function setLanguage(lang) {
    localStorage.setItem('eureka_lang', lang);
    applyTranslations(lang);
}

function applyTranslations(lang) {
    const dict = translations[lang] || translations['en'];
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = dict[key];
            } else {
                // To preserve inner icons like <i class="..."></i>, we only replace text nodes?
                // Actually it's easier to put the icon OUTSIDE the span with data-i18n
                el.innerText = dict[key];
            }
        }
    });

    // Toggle button active states
    const btnEn = document.getElementById('btnLangEn');
    const btnKn = document.getElementById('btnLangKn');
    if (btnEn && btnKn) {
        if (lang === 'kn') {
            btnKn.classList.add('active-lang');
            btnEn.classList.remove('active-lang');
        } else {
            btnEn.classList.add('active-lang');
            btnKn.classList.remove('active-lang');
        }
    }
}

// Run on load
document.addEventListener('DOMContentLoaded', () => {
    const lang = localStorage.getItem('eureka_lang') || 'en';
    applyTranslations(lang);
});
